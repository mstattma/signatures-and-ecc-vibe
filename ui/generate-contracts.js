/**
 * Generate externalContracts.ts from deployment.json + Hardhat artifacts.
 * Runs inside the UI Docker container at startup.
 *
 * Usage: node generate-contracts.js <deployment.json> <artifacts-dir>
 */

const fs = require("fs");
const path = require("path");

const deploymentPath = process.argv[2];
const artifactsDir = process.argv[3];
const outputPath = path.join(__dirname, "packages", "nextjs", "contracts", "externalContracts.ts");

const CONTRACTS = {
  KeyRegistry: "keyRegistry",
  CrossChainBloomFilter: "bloomFilter",
  ImageAuthResolver: "resolver",
  ReputationRegistry: "reputationRegistry",
  EAS: "eas",
  SchemaRegistry: "schemaRegistry",
};

function main() {
  let deployment = {};
  try {
    deployment = JSON.parse(fs.readFileSync(deploymentPath, "utf8"));
    console.log("  Deployment:", deployment.network, deployment.timestamp);
  } catch (e) {
    console.error("  Cannot read deployment.json:", e.message);
    return;
  }

  const contracts = {};
  for (const [name, key] of Object.entries(CONTRACTS)) {
    const address = deployment[key];
    if (!address || address === "0x0000000000000000000000000000000000000000") {
      console.log(`  ${name}: skipped (no address)`);
      continue;
    }

    let artifactPath = path.join(artifactsDir, "contracts", `${name}.sol`, `${name}.json`);
    if (!fs.existsSync(artifactPath) && (name === "EAS" || name === "SchemaRegistry")) {
      artifactPath = path.join(
        artifactsDir,
        "@ethereum-attestation-service",
        "eas-contracts",
        "contracts",
        `${name}.sol`,
        `${name}.json`,
      );
    }
    if (!fs.existsSync(artifactPath)) {
      console.log(`  ${name}: skipped (no artifact at ${artifactPath})`);
      continue;
    }

    const artifact = JSON.parse(fs.readFileSync(artifactPath, "utf8"));
    contracts[name] = { address, abi: artifact.abi };
    console.log(`  ${name}: ${address} (${artifact.abi.length} ABI entries)`);
  }

  if (Object.keys(contracts).length === 0) {
    console.log("  No contracts found, keeping existing externalContracts.ts");
    return;
  }

  const contractEntries = Object.entries(contracts)
    .map(([name, { address, abi }]) =>
      `    ${name}: {\n      address: "${address}",\n      abi: ${JSON.stringify(abi)},\n    }`
    )
    .join(",\n");

  const output = `import { GenericContractsDeclaration } from "~~/utils/scaffold-eth/contract";

/**
 * Auto-generated by generate-contracts.js at container startup.
 * Timestamp: ${new Date().toISOString()}
 */
const externalContracts = {
  31337: {
${contractEntries}
  },
} as const;

export default externalContracts satisfies GenericContractsDeclaration;
`;

  fs.writeFileSync(outputPath, output);
  console.log(`  Written to ${outputPath}`);
}

main();
